public class task1 {
    public static void main(String[] args) {
        //use the cycle for
        for (int a = 500; a <= 650; a += 10){
            System.out.println(a);
        }

        //use the cycle while
        int b = 500;
        while (b <= 650){
            System.out.println(b);
            b += 10;
        }

        //use the cycle do-while
        int c = 500;
        do{
            System.out.println(c);
            c += 10;
        } while (c <= 650);
    }


}
